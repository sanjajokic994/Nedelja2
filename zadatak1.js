let a=8;
let b=5;
let znak='**'
switch(znak){
case '+':
    console.log(a+b);
    break;
case '-':
    console.log(a-b);
    break;
case '*':
    console.log(a*b);
    break;
case '/':
    console.log(a/b);
    break;
case '%':
    console.log(a%b);
    break;
case '**':  
    console.log(a**b);
    break;
}